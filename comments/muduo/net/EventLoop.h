#ifndef MUDUO_NET_EVENTLOOP_H
#define MUDUO_NET_EVENTLOOP_H

#include <vector>

#include <boost/any.hpp>
#include <boost/function.hpp>
#include <boost/noncopyable.hpp>
#include <boost/scoped_ptr.hpp>

#include <muduo/base/Mutex.h>
#include <muduo/base/CurrentThread.h>
#include <muduo/base/Timestamp.h>
#include <muduo/net/Callbacks.h>
#include <muduo/net/TimerId.h>

namespace muduo
{
namespace net
{

class Channel;
class Poller;
class TimerQueue;

///
/// Reactor, at most one per thread.  一个线程最多一个ractor
///
/// This is an interface class, so don't expose too much details.

//接口类
class EventLoop : boost::noncopyable
{
 public:
  typedef boost::function<void()> Functor;   //类型重定义

  EventLoop();
  ~EventLoop();  // force out-line dtor, for scoped_ptr members.

  ///
  /// Loops forever.
  ///
  /// Must be called in the same thread as creation of the object.
  /// 
  void loop();

  /// Quits loop.
  ///
  /// This is not 100% thread safe, if you call through a raw pointer,
  /// better to call through shared_ptr<EventLoop> for 100% safety.
  void quit();

  ///
  /// Time when poll returns, usually means data arrival.
  ///
  Timestamp pollReturnTime() const { return pollReturnTime_; }

  int64_t iteration() const { return iteration_; }

  /// Runs callback immediately in the loop thread.
  /// It wakes up the loop, and run the cb.
  /// If in the same loop thread, cb is run within the function.
  /// Safe to call from other threads.
  // 仔细阅读上面
  void runInLoop(const Functor& cb);
  /// Queues callback in the loop thread.
  /// Runs after finish pooling.
  /// Safe to call from other threads.
  void queueInLoop(const Functor& cb);

  size_t queueSize() const;

#ifdef __GXX_EXPERIMENTAL_CXX0X__
  void runInLoop(Functor&& cb);
  void queueInLoop(Functor&& cb);
#endif

  // timers

  ///
  /// Runs callback at 'time'.
  /// Safe to call from other threads.
  ///
  //在某一时刻运行定时器回调函数
  TimerId runAt(const Timestamp& time, const TimerCallback& cb);
  ///
  /// Runs callback after @c delay seconds.
  /// Safe to call from other threads.
  ///
  //过一段时间运行定时器回调函数
  TimerId runAfter(double delay, const TimerCallback& cb);
  ///
  /// Runs callback every @c interval seconds.
  /// Safe to call from other threads.
  ///
  //每隔一段时间运行定时器回掉函数
  TimerId runEvery(double interval, const TimerCallback& cb);
  ///
  /// Cancels the timer.
  /// Safe to call from other threads.
  ///
  void cancel(TimerId timerId);

#ifdef __GXX_EXPERIMENTAL_CXX0X__
  TimerId runAt(const Timestamp& time, TimerCallback&& cb);
  TimerId runAfter(double delay, TimerCallback&& cb);
  TimerId runEvery(double interval, TimerCallback&& cb);
#endif

  // internal usage
  void wakeup();
  void updateChannel(Channel* channel);  //在poller中增加或更新通道
  void removeChannel(Channel* channel);  //从poller中移除通道
  bool hasChannel(Channel* channel);

  // pid_t threadId() const { return threadId_; }
  //断言是在当前线程
  void assertInLoopThread()
  {
    if (!isInLoopThread())
    {
      abortNotInLoopThread();
    }
  }
  bool isInLoopThread() const { return threadId_ == CurrentThread::tid(); }
  // bool callingPendingFunctors() const { return callingPendingFunctors_; }
  bool eventHandling() const { return eventHandling_; }

  void setContext(const boost::any& context)
  { context_ = context; }

  const boost::any& getContext() const
  { return context_; }

  boost::any* getMutableContext()
  { return &context_; }

  static EventLoop* getEventLoopOfCurrentThread();

 private:
  void abortNotInLoopThread();
  void handleRead();  // waked up
  void doPendingFunctors();

  void printActiveChannels() const; // DEBUG

  typedef std::vector<Channel*> ChannelList;


  //在linux下bool类型本来就是原子操作跟整形不一样!所以不需要加锁
  bool looping_; /* atomic */	//是否处于循环
  bool quit_; /* atomic and shared between threads, okay on x86, I guess. */
  bool eventHandling_; /* atomic */  //是否处于事件处理状态
  bool callingPendingFunctors_; /* atomic */
  int64_t iteration_;
  const pid_t threadId_;	// 当前对象所属线程ID
  Timestamp pollReturnTime_;  //调用poll函数所返回的时间戳
  boost::scoped_ptr<Poller> poller_;    //只能指针对象，管理poller
  boost::scoped_ptr<TimerQueue> timerQueue_;   //管理定时器的TimeQueue
  int wakeupFd_;   //用于evenfd,参见man(2)
  // unlike in TimerQueue, which is an internal class,
  // we don't expose Channel to client.
  //只有整个channel是组合关系，负责管理wakeupChannel的生命周期
  boost::scoped_ptr<Channel> wakeupChannel_; //该通道会纳入poller_来管理
  boost::any context_;

  // scratch variables
  //channelList与EvenLoop是聚合关系，并不管理ChannelList中元素的生命周期
  ChannelList activeChannels_;      //当前返回的活动通道集合
  Channel* currentActiveChannel_;   //当前正在处理的活动通道

  mutable MutexLock mutex_;      //pendingFunctors的锁
  //一个任务队列
  std::vector<Functor> pendingFunctors_; // @GuardedBy mutex_
};

}
}
#endif  // MUDUO_NET_EVENTLOOP_H
